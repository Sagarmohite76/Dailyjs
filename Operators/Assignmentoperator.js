// Assignment operator
// 1. is equal to
let a = 45;
console.log(a);

let b = 10;
console.log(b);

let name = 'John';
console.log(name);

let isActive = true;
console.log(isActive);

let fruits = 'apple';
console.log(fruits);

// 2. add is equal to (+=)
let z = 45;
z += 10;
console.log(z);

let Name = 'John';
Name += ' Doe';
console.log(Name);

let score = 20;
score += 30;
console.log(score);

let total = 100;
total += 50;
console.log(total);

let greeting = 'Hello, ';
greeting += 'world!';
console.log(greeting);

// 3. subtract is equal to (-=)
let value = 5;
value -= 10;
console.log(value);

let points = 200;
points -= 40;
console.log(points);

let budget = 1000;
budget -= 200;
console.log(budget);

let balance = 750;
balance -= 150;
console.log(balance);

let items = 25;
items -= 7;
console.log(items);


