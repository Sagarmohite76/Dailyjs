// Primitive data types
// 1.number
let num=25;
console.log(num);          // output=25
console.log(typeof num);   // output=number

// 2. string
var str="Javascript";
console.log(str);
console.log(typeof str);   // string

// 3. boolean
let x=20;
let y=50;
let z=x>y;
console.log(z);          // output:: false
console.log(typeof z)    // outut :: Boolean

// 4. Undefine         
                  // Value not be assigned so it is undefined               
let b;
console.log(typeof b);   // Undefined


// 5.null
let Value=null;        // There is no value in variable
console.log(typeof Value);  // object