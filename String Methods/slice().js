let sentence1 = "Hello, world!";
console.log(sentence1.slice(7, 12));  // Output: "world"

let sentence2 = "JavaScript is fun!";
console.log(sentence2.slice(4, 10));  // Output: "Script"

let sentence3 = "The quick brown fox jumps.";
console.log(sentence3.slice(0, 3));  // Output: "The"

let sentence4 = "Coding challenges are cool!";
console.log(sentence4.slice(-6, -1));  // Output: "cool"

let sentence5 = "Good morning, everyone!";
console.log(sentence5.slice(-9));  // Output: "everyone!"
