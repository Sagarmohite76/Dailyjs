let str1 = "Hello";
let str2 = "world!";
console.log(str1.concat(", ",str2));  // Output: "Hello, world!"

let firstName = "John";
let lastName = "Doe";
console.log(firstName.concat(" ", lastName));  // Output: "John Doe"

let greeting = "Good";
let timeOfDay = "morning";
console.log(greeting.concat(", ", timeOfDay, "!"));  // Output: "Good, morning!"

let part1 = "Java";
let part2 = "Script";
let exclamation = " rocks!";
console.log(part1.concat(part2, exclamation));  // Output: "JavaScript rocks!"

let city = "New";
let country = "York";
console.log(city.concat(" ", country));  // Output: "New York"
