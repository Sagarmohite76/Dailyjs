let word1 = "Hello";
console.log(word1.repeat(3));  // Output: "HelloHelloHello"

let phrase = "JavaScript ";
console.log(phrase.repeat(2));  // Output: "JavaScript JavaScript "

let exclamation = "Wow! ";
console.log(exclamation.repeat(4));  // Output: "Wow! Wow! Wow! Wow! "

let star = "*";
console.log(star.repeat(10));  // Output: "**********"

let sentence = "Repeat this. ";
console.log(sentence.repeat(0));  // Output: ""
